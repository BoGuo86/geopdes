	<section align="justify">
	More than forty years ago, two institutions, the Laboratoire d'Analyse Num&eacute;rique in 
	Paris and the Laboratorio di Analisi Numerica (later renamed Istituto di Analisi Numerica) 
	in Pavia, were founded respectively by Jacques-Louis Lions and Enrico Magenes 
	with the common objective of facing questions related to the analysis and 
	the numerical solution of partial differential equations. Both institutions 
	were later renamed after their respective founder: the Laboratoire d'Analyse 
	Num&eacute;rique takes in 2002 the name Laboratoire Jacques-Louis Lions, while the 
	Istituto di Matematica Applicata e Tecnologie Informatiche (resulting from 
	the fusion of the Istituto di Analisi Numerica with two other CNR institutes,  
	IAM and IAMI) saw in 2011, the name of Enrico Magenes added to its official 
	denomination. The two founders, who  shared a most prolific scientific 
	relation as well as a profound friendship, wrote a great number of pages 
	that can be considered as fundamental in the history of applied analysis 
	(both theoretical and numerical) of PDE's. Under their guidance the 
	collaboration between the two laboratories developed along different 
	generations and resulted in a vast production in the field with a great 
	number of joint papers.<br><br>
